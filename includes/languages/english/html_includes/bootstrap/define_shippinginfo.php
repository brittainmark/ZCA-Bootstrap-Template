<h2>Refunds</h2>
<p>We hope that your order will reach you in the same condition it left
	us, however occasionally some damage may occur during transit. Your
	delivery options will have ensured that all despatches are wherever
	possible covered by Royal Mail's insurance terms and conditions. If any
	items have obviously been damaged in the delivery process, please
	contact us immediately, stating the problem, before you send the
	item(s) back. We will then let you know the procedure to be followed
	regarding return and refund of costs.</p>
